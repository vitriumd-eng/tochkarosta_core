"""
User Roles Enum

Роли пользователей в системе (иерархия от высшей к низшей):

1. super_admin (Основатель) - владелец платформы, управляет платформой и ядром
2. platform_master (Модератор) - модератор контента платформы, управляет контентом платформеной страницы
3. user (Пользователь) - публичный пользователь, просто смотрит платформеную страницу, не зарегистрирован
4. subscriber (Подписчик) - использует подписку, арендует платформу для своего бизнеса
5. buyer (Покупатель) - клиент подписчика, покупает товары/услуги у подписчика
"""
import enum


class UserRole(str, enum.Enum):
    """
    User roles enum
    
    Иерархия ролей:
    1. super_admin: Основатель/владелец платформы (Founder/Owner)
    2. platform_master: Модератор контента платформы (Platform Content Moderator)
    3. user: Публичный пользователь (Public User) - просматривает платформеную страницу без регистрации
    4. subscriber: Подписчик (Subscription User) - арендует платформу
    5. buyer: Покупатель (Customer/Buyer) - клиент подписчика
    """
    # Основатель/владелец платформы - управляет платформой и ядром
    super_admin = "super_admin"
    
    # Модератор контента платформы - управляет контентом платформеной страницы
    platform_master = "platform_master"
    
    # Публичный пользователь - просто смотрит платформеную страницу, не зарегистрирован
    user = "user"
    
    # Подписчик - использует подписку, арендует платформу для своего бизнеса
    subscriber = "subscriber"
    
    # Покупатель - клиент подписчика, покупает товары/услуги
    buyer = "buyer"

